def calculate_circle_area(radius):
  area = 3,14 * radius * radius
  return area

def calculate_rectangle_area(length, width):
  area = length * width
  return area

def calculate_sphere_area(radius):
  area = 4 * 3.14 * radius * radius
  return area

def calculate_cube_area(side):
  area = 6 * side * side
  return area